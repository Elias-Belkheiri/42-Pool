int	ft_atoi(char *str)
{
	int	i;
	int	sign;
	int	k;

	k = 0;
	sign = 1;
	i = -1;
	while (str[++i])
	{
		while ((str[i] >= 9 && str[i] <= 13) || str[i] == 32)
			i++;
		while (str[i] == '-' || str[i] == '+')
		{
			if (str[i] == '-')
				sign *= -1;
			i++;
		}
		while (str[i] >= '0' && str[i] <= '9')
			k = k * 10 + str[i++] - 48;
		if (!(str[i] >= '0' && str[i] <= '9'))
			break ;
	}
	return (k * sign);
}
