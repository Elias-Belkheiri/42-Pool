int	ft_iterative_factorial(int nb)
{
	int	n;

	if (nb == 0)
		return (1);
	else if (nb > 0)
	{
		n = nb - 1;
		while (n > 0)
		{
			nb *= n;
			n--;
		}
		return (nb);
	}
	return (0);
}
