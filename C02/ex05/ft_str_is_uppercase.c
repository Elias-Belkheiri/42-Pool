int	ft_str_is_uppercase(char *str)
{
	int	i;
	int	k;

	i = 0;
	if (str[0] == '\0')
		k = 1;
	while (str[i] != '\0')
	{
		if (str[i] >= 'A' && str[i] <= 'Z')
			k = 1;
		else
		{
			k = 0;
			break ;
		}
		i++;
	}
	return (k);
}
